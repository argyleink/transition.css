<script>
	import Logo from './components/Logo.svelte'
	import Sidebar from './components/Sidebar.svelte'
	import {transition, duration, easing} from './store.js'

	let active_transition = transition
	let last_chosen = transition
	let speed = duration
	let ease = easing

	transition.subscribe(txn => {
		last_chosen = txn
		active_transition = txn
	})

	function demoClick() {
		active_transition = active_transition.includes('out')	
			? active_transition.replace('out','in')	
			: active_transition.replace('in','out')
	}
</script>

<main>

	<section class="backdrop" on:click={demoClick}>
		<Logo backdrop=true/>
	</section>

	<section 
		transition-style="{active_transition}" 
		on:click={demoClick}
		style="
